public class VariableDemo {
    public static void main(String[] args) {
        int a = 10;
        double b = 1.01;
        System.out.println(a);
        System.out.println(b);
        System.out.println(a + b);
        a = 20;
        System.out.println(a + b);
        int c = 1, d = 2, f = 3;
        System.out.println(c + d + f);
    }
}
